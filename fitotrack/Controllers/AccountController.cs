        //
        // POST: /Account/Login
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model, string returnUrl)
        {
            if (model == null || !ModelState.IsValid)
            {
                return View(model);
            }

            // Для включения блокировки пользователя после определенного количества неправильных попыток
            // необходимо последним (4-м) параметром передать shouldLockout: true.
            var result = await Repo.Login(model.Email, model.Password, model.RememberMe);
            switch (result)
            {
                case SignInStatus.Success:
                    return RedirectToLocal(returnUrl);
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.RequiresVerification:
                    return RedirectToAction("SendCode", new { ReturnUrl = returnUrl, RememberMe = model.RememberMe });
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Incorrect Email or Password.");
                    return View(model);
            }
        }

